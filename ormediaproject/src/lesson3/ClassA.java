package lesson3;

public class ClassA {
	
	private String name;
	private int ValueA ;
	
	
	
	public ClassA(){}
	
	public ClassA(String name, int ValueA){
		this.name = name;
		this.ValueA = ValueA;
		
		
	}
	
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		name = name;
	}

	public int getValueA() {
		return ValueA;
	}

	public void setValueA(int ValueA) {
		ValueA = ValueA;
	}

	
	
	
	
	
	
	

}
